public class CalculatorLogic 
{
    public int add(int a, int b) 
    {
        return (a+b);
    }
    
    public int sub(int c,int d)
    {
    	return (c-d);
    }
    
    public int mul(int e,int f)
    {
    	return (e*f);
    }
    
    public int div(int g, int h)
    {
    	return (g/h);
    }
}
